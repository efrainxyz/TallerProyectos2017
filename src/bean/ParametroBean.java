package bean;

public class ParametroBean {
	
	private  String codigoEscuela;
	private String codigoSemestre;
	private int ciclo;
	private int creditos;
	public String getCodigoEscuela() {
		return codigoEscuela;
	}
	public void setCodigoEscuela(String codigoEscuela) {
		this.codigoEscuela = codigoEscuela;
	}
	public String getCodigoSemestre() {
		return codigoSemestre;
	}
	public void setCodigoSemestre(String codigoSemestre) {
		this.codigoSemestre = codigoSemestre;
	}
	public int getCiclo() {
		return ciclo;
	}
	public void setCiclo(int ciclo) {
		this.ciclo = ciclo;
	}
	public int getCreditos() {
		return creditos;
	}
	public void setCreditos(int creditos) {
		this.creditos = creditos;
	}

}
