package bean;

public class SemenstreBean {
	
	private String idSemestre;
	private int ano;
	private int semestre;
	private int cantidadSemanas;
	public String getIdSemestre() {
		return idSemestre;
	}
	public void setIdSemestre(String idSemestre) {
		this.idSemestre = idSemestre;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public int getSemestre() {
		return semestre;
	}
	public void setSemestre(int semestre) {
		this.semestre = semestre;
	}
	public int getCantidadSemanas() {
		return cantidadSemanas;
	}
	public void setCantidadSemanas(int cantidadSemanas) {
		this.cantidadSemanas = cantidadSemanas;
	}

}
